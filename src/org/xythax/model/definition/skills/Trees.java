package org.xythax.model.definition.skills;

public class Trees {

	private int treeId;
	private int level;
	private int log;
	private int minimumAxe;
	private int xp;
	private int stumpId;
	private int logAmount;
	private int respawnTime;

	public int getTreeId() {
		return treeId;
	}

	public int getLevel() {
		return level;
	}

	public int getLog() {
		return log;
	}

	public int getMinimumAxe() {
		return minimumAxe;
	}

	public int getXp() {
		return xp;
	}

	public int getStumpId() {
		return stumpId;
	}

	public int getLogAmount() {
		return logAmount;
	}

	public int getRespawnTime() {
		return respawnTime;
	}
}