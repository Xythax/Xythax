package org.xythax.model.definition.skills;

public class Axes {

	/**
	 * The hatchets id.
	 */
	private int axeId;
	/**
	 * The hatchets animation.
	 */
	private int axeAnimation;
	/**
	 * The hatchets strength.
	 */
	private int axeStrength;

	/**
	 * Gets id of the hatchet.
	 * 
	 * @return axeId
	 */
	public int getAxeId() {
		return axeId;
	}

	/**
	 * The animation of the axe.
	 * 
	 * @return axeAnimation
	 */
	public int getAxeAnimation() {
		return axeAnimation;
	}

	/**
	 * The strength of the axe.
	 * 
	 * @return axeStrength
	 */
	public int getAxeStrength() {
		return axeStrength;
	}
}
